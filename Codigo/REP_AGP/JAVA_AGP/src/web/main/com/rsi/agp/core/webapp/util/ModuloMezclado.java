package com.rsi.agp.core.webapp.util;

public class ModuloMezclado {
	private String codPpal;
	private String codRiesgo;
	private String concepto;
	private String valor;
	public String getCodPpal() {
		return codPpal;
	}
	public void setCodPpal(String codPpal) {
		this.codPpal = codPpal;
	}
	public String getCodRiesgo() {
		return codRiesgo;
	}
	public void setCodRiesgo(String codRiesgo) {
		this.codRiesgo = codRiesgo;
	}
	public String getConcepto() {
		return concepto;
	}
	public void setConcepto(String concepto) {
		this.concepto = concepto;
	}
	public String getValor() {
		return valor;
	}
	public void setValor(String valor) {
		this.valor = valor;
	}
	
	

}
