package com.rsi.agp.dao.tables.poliza;

public class TipoCapitalComparativa {
	
	private String produccion;
	private String valorProduccion;
	private String costeTomador;
	private String costeUnitario;
	private String codModulo;
	
	public String getProduccion() {
		return produccion;
	}
	public void setProduccion(String produccion) {
		this.produccion = produccion;
	}
	public String getValorProduccion() {
		return valorProduccion;
	}
	public void setValorProduccion(String valorProduccion) {
		this.valorProduccion = valorProduccion;
	}
	public String getCosteTomador() {
		return costeTomador;
	}
	public void setCosteTomador(String costeTomador) {
		this.costeTomador = costeTomador;
	}
	public String getCosteUnitario() {
		return costeUnitario;
	}
	public void setCosteUnitario(String costeUnitario) {
		this.costeUnitario = costeUnitario;
	}
	public String getCodModulo() {
		return codModulo;
	}
	public void setCodModulo(String codModulo) {
		this.codModulo = codModulo;
	}
}
