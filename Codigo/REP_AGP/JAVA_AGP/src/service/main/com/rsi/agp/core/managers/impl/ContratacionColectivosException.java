package com.rsi.agp.core.managers.impl;

import javax.xml.ws.WebServiceException;


public class ContratacionColectivosException extends WebServiceException {
	
	public ContratacionColectivosException() {
		super();
	}

	public ContratacionColectivosException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public ContratacionColectivosException(String arg0) {
		super(arg0);
	}

	public ContratacionColectivosException(Throwable arg0) {
		super(arg0);
	}

}
