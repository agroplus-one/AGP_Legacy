package com.rsi.agp.core.managers.impl.anexoMod.calculo;

import javax.xml.ws.WebServiceException;

public class CalculoModificacionException extends WebServiceException {
	
	public CalculoModificacionException() {
		super();
	}

	public CalculoModificacionException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public CalculoModificacionException(String arg0) {
		super(arg0);
	}

	public CalculoModificacionException(Throwable arg0) {
		super(arg0);
	}
}
