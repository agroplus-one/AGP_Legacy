package com.rsi.agp.core.exception;

public class PrecioGanadoException extends Exception {

	private static final long serialVersionUID = 8005903548138754385L;

	public PrecioGanadoException() {
		super();
	}

	public PrecioGanadoException(String message, Throwable cause) {
		super(message, cause);
	}

	public PrecioGanadoException(String message) {
		super(message);
	}

	public PrecioGanadoException(Throwable cause) {
		super(cause);
	}
	
	

}
